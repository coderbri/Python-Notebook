len("Hello")    # 5

# Subscripting
print("Hello"[0])       # H
print("Hello"[4])       # o
print("Hello"[-2])      # l

# Integer = Whole Number
print(123 + 456)        # 468
print(123_456_789)      # 123456789

# Float = Floating Point Number
print(3.14159)

# Boolean = True/False
print(True)
print(False)